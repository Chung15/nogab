<h2>Mbolo &laquo;&nbsp;Bienvenue&nbsp;&raquo;</h2>
<div class="date">
          2010 <br>
          04.03</div>
      <div class="entry">
        <p>Bonjour à tous,</p>
<p>Le but de ce blog est de faire découvrir aux internautes les variétés de fruits et légumes exotiques.<br />
Pour cela, je propose une petite escale au Gabon, mon pays d&#8217;origine.</p>
<div id="attachment_145" class="wp-caption aligncenter" style="width: 550px"><a href="http://lapetuite.free.fr/wp-content/uploads/accueil-blog1.jpg" rel="lightbox[112]"><img class="size-full wp-image-145  " title="Le contenu de nos assiettes " src="http://lapetuite.free.fr/wp-content/uploads/accueil-blog1.jpg" alt="Le contenu de nos assiettes " width="540" height="400" /></a><p class="wp-caption-text">Le contenu de nos assiettes </p></div>
<p><strong>Gabon?<br />
</strong></p>
<p>C&#8217;est un pays situé dans le centre de  l&#8217;Afrique.</p>
<p>Sa particularité: il est traversé par l&#8217;équateur. Ce qui fait de ce pays un des plus riches en biodiversité.</p>
<p>C’est un pays forestier où la faune et la flore sont encore bien  conservées.</p>
<p>Vous pourriez découvrir à travers ce blog, certains fruits et légumes cultivés au Gabon. Ces fruits et légumes appelés exotiques que nous retrouvons dans les rayons fruits et légumes de grandes surfaces ou magasins spécialisés en France.</p>
<p>Sachant que ce domaine de l&#8217;exportation ne se trouve pas dans les compétences premières du Gabon, ces fruits et légumes ne proviennent donc pas du Gabon. La population gabonaise se contentent de produire le strict minimum pour nourrir en priorité la famille.</p>

        
